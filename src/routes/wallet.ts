/**
 * POST /wallet/prepare-create-session
 * Prepare TX to create a session key for an agent.
 *
 * The agent (bot keypair) is the signer/payer for session creation on-chain.
 * However, since the Flutter app signs via MWA (owner wallet), we prepare
 * the TX for the OWNER to sign — the agent keypair is stored server-side
 * and we don't require it to sign here. The Flutter app must ensure the
 * agent keypair also signs before submission.
 *
 * Flow:
 * 1. Validate bot exists, belongs to user, and has a registered agent
 * 2. Verify the bot doesn't already have an active session
 * 3. Derive the SessionKey PDA from (walletPda, agentPubkey, sessionPubkey)
 * 4. Build CreateSession instruction
 * 5. Return serialized TX for client-side signing (agent signs)
 * 6. Store sessionAddress in bot row
 */
wallet.post(
  "/prepare-create-session",
  requireAuth,
  zValidator("json", createSessionSchema),
  async (c) => {
    const userId = c.var.userId;
    const ownerAddress = c.var.walletAddress;
    const body = c.req.valid("json");

    const ownerPubkey = new PublicKey(ownerAddress);
    const sessionPubkey = new PublicKey(body.sessionPubkey);

    // ── Validate bot ──
    const [bot] = await db
      .select()
      .from(bots)
      .where(and(eq(bots.botId, body.botId), eq(bots.userId, userId)));

    if (!bot) {
      throw createApiError("Bot not found", 404);
    }

    if (bot.mode !== "live") {
      throw createApiError(
        "Session keys are only used for live-mode bots",
        400
      );
    }

    if (!bot.agentPubkey || !bot.agentConfigAddress) {
      throw createApiError(
        "Bot must have a registered agent before creating a session. " +
        "Use /wallet/prepare-register-agent first.",
        400
      );
    }

    if (bot.sessionAddress) {
      throw createApiError("Bot already has an active session", 409, {
        sessionAddress: bot.sessionAddress,
      });
    }

    // ── Verify Seal wallet exists on-chain ──
    const walletPda = await getCanonicalWalletPda(userId, ownerPubkey);
    const connection = getConnection();
    const walletAccount = await withRpcRetry(() =>
      connection.getAccountInfo(walletPda)
    );
    if (!walletAccount) {
      throw createApiError(
        "Seal wallet not found. Create one first.",
        404
      );
    }

    // ── Derive PDAs ──
    const agentPubkey = new PublicKey(bot.agentPubkey);
    const [agentPda] = deriveAgentPda(walletPda, agentPubkey);

    // Verify agent config exists on-chain
    const agentAccount = await withRpcRetry(() =>
      connection.getAccountInfo(agentPda)
    );
    if (!agentAccount) {
      throw createApiError(
        "Agent config not found on-chain. Register the agent first.",
        404,
        { expectedAddress: agentPda.toBase58() }
      );
    }

    const [sessionPda, bump] = deriveSessionPda(
      walletPda,
      agentPubkey,
      sessionPubkey
    );

    // Verify session PDA doesn't already exist on-chain
    const existingSession = await withRpcRetry(() =>
      connection.getAccountInfo(sessionPda)
    );
    if (existingSession) {
      throw createApiError("Session account already exists on-chain", 409, {
        sessionAddress: sessionPda.toBase58(),
      });
    }

    // ── Build TX ──
    const durationSecs = BigInt(body.durationSecs);
    const maxAmountLamports = BigInt(
      Math.floor(body.maxAmountSol * LAMPORTS_PER_SOL)
    );
    const maxPerTxLamports = BigInt(
      Math.floor(body.maxPerTxSol * LAMPORTS_PER_SOL)
    );

    // Validate spending limits: session limits should not exceed what
    // was set at agent registration level
    if (maxPerTxLamports > maxAmountLamports) {
      throw createApiError(
        "maxPerTxSol cannot exceed maxAmountSol",
        400
      );
    }

    const ix = buildCreateSessionIx(
      agentPubkey,  // agent is signer/payer on-chain
      walletPda,
      agentPda,
      sessionPda,
      bump,
      sessionPubkey,
      durationSecs,
      maxAmountLamports,
      maxPerTxLamports
    );

    const { blockhash, lastValidBlockHeight } = await withRpcRetry(() =>
      connection.getLatestBlockhash()
    );

    const tx = new Transaction();
    tx.add(ix);
    tx.recentBlockhash = blockhash;
    // Agent is the fee payer for session creation (they're the signer on-chain)
    tx.feePayer = agentPubkey;

    const serialized = tx
      .serialize({ requireAllSignatures: false })
      .toString("base64");

    // ── Persist session info in bot row ──
    await db.update(bots)
      .set({
        sessionAddress: sessionPda.toBase58(),
        updatedAt: new Date(),
      })
      .where(eq(bots.botId, body.botId));

    return c.json({
      success: true,
      botId: body.botId,
      sessionAddress: sessionPda.toBase58(),
      sessionPubkey: sessionPubkey.toBase58(),
      agentPubkey: bot.agentPubkey,
      durationSecs: body.durationSecs,
      maxAmountSol: body.maxAmountSol,
      maxPerTxSol: body.maxPerTxSol,
      transaction: serialized,
      blockhash,
      lastValidBlockHeight,
    });
  }
);

