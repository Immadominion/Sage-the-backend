/** Encode a string as fixed-length bytes (zero-padded) — matches seal-ts */
function encodeFixedString(str: string, length: number): Buffer {
  const buf = Buffer.alloc(length);
  buf.write(str, "utf-8");
  return buf;
}

/** Encode an i64 as little-endian 8 bytes */
function encodeI64(value: bigint): Buffer {
  const buf = Buffer.alloc(8);
  buf.writeBigInt64LE(value);
  return buf;
}

const MAX_AGENT_NAME_LENGTH = 32;

/**
 * Build a RegisterAgent instruction (discriminant = 1).
 *
 * Binary layout:
 *   [0]       disc (1)
 *   [1]       bump
 *   [2..33]   agent pubkey (32 bytes)
 *   [34..65]  name (32 bytes, zero-padded)
 *   [66]      allowedPrograms count
 *   [67..]    allowedPrograms (32 bytes each)
 *   [N]       allowedInstructions count
 *   [N+1..]   allowedInstructions (8 bytes each)
 *   [..]      dailyLimit (u64le)
 *   [..]      perTxLimit (u64le)
 *   [..]      defaultSessionDuration (i64le)
 *   [..]      maxSessionDuration (i64le)
 */
function buildRegisterAgentIx(
  owner: PublicKey,
  walletPda: PublicKey,
  agentPubkey: PublicKey,
  agentPda: PublicKey,
  bump: number,
  name: string,
  dailyLimitLamports: bigint,
  perTxLimitLamports: bigint,
  allowedPrograms: PublicKey[] = [],
  allowedInstructions: Buffer[] = []
) {
  // Defaults matching seal-ts
  const defaultSessionDuration = BigInt(24 * 60 * 60); // 24 hours
  const maxSessionDuration = BigInt(7 * 24 * 60 * 60); // 7 days

  const data = Buffer.concat([
    Buffer.from([1]), // InstructionDiscriminant.RegisterAgent
    Buffer.from([bump]),
    agentPubkey.toBuffer(),
    encodeFixedString(name, MAX_AGENT_NAME_LENGTH),
    Buffer.from([allowedPrograms.length]),
    ...allowedPrograms.map((programId) => programId.toBuffer()),
    Buffer.from([allowedInstructions.length]),
    ...allowedInstructions,
    encodeU64(dailyLimitLamports),
    encodeU64(perTxLimitLamports),
    encodeI64(defaultSessionDuration),
    encodeI64(maxSessionDuration),
  ]);

  return {
    programId: SEAL_PROGRAM_ID,
    keys: [
      { pubkey: owner, isSigner: true, isWritable: true },
      { pubkey: walletPda, isSigner: false, isWritable: true },
      { pubkey: agentPda, isSigner: false, isWritable: true },
      {
        pubkey: SystemProgram.programId,
        isSigner: false,
        isWritable: false,
      },
    ],
    data,
  };
}

/**
 * Build a CreateSession instruction (discriminant = 2).
 *
 * On-chain layout (57 bytes):
 *   [0]       disc (2)
 *   [1]       bump
 *   [2..33]   session_pubkey (32 bytes)
 *   [34..41]  duration_secs (i64 LE)
 *   [42..49]  max_amount_lamports (u64 LE)
 *   [50..57]  max_per_tx_lamports (u64 LE)
 *
 * Accounts (matching seal program create_session.rs):
 *   0. [signer, writable] Agent
 *   1. []                 SmartWallet PDA
 *   2. []                 AgentConfig PDA
 *   3. [writable]         SessionKey PDA
 *   4. []                 System Program
 */
function buildCreateSessionIx(
  agentPubkey: PublicKey,
  walletPda: PublicKey,
  agentPda: PublicKey,
  sessionPda: PublicKey,
  bump: number,
  sessionPubkey: PublicKey,
  durationSecs: bigint,
  maxAmountLamports: bigint,
  maxPerTxLamports: bigint
) {
  const data = Buffer.concat([
    Buffer.from([2]), // InstructionDiscriminant.CreateSessionKey
    Buffer.from([bump]),
    sessionPubkey.toBuffer(),
    encodeI64(durationSecs),
    encodeU64(maxAmountLamports),
    encodeU64(maxPerTxLamports),
  ]);

  return {
    programId: SEAL_PROGRAM_ID,
    keys: [
      { pubkey: agentPubkey, isSigner: true, isWritable: true },
      { pubkey: walletPda, isSigner: false, isWritable: false },
      { pubkey: agentPda, isSigner: false, isWritable: false },
      { pubkey: sessionPda, isSigner: false, isWritable: true },
      {
        pubkey: SystemProgram.programId,
        isSigner: false,
        isWritable: false,
      },
    ],
    data,
  };
}

async function finalizeLiveSession(opts: {
  botId: string;
  userId: number;
  walletPda: PublicKey;
  durationSecs: number;
  maxAmountSol: number;
  maxPerTxSol: number;
}): Promise<{
  sessionAddress: string;
  sessionPubkey: string;
  signature: string;
}> {
  const connection = getConnection();

  const [bot] = await db
    .select()
    .from(bots)
    .where(and(eq(bots.botId, opts.botId), eq(bots.userId, opts.userId)));

  if (!bot?.agentPubkey || !bot.agentSecretKey || !bot.sessionPubkey || !bot.sessionSecretKey) {
    throw createApiError("Live setup keys are missing. Retry setup.", 409);
  }

  const agentPubkey = new PublicKey(bot.agentPubkey);
  const sessionPubkey = new PublicKey(bot.sessionPubkey);
  const agentKeypair = Keypair.fromSecretKey(
    Buffer.from(bot.agentSecretKey, "base64")
  );
  const sessionKeypair = Keypair.fromSecretKey(
    Buffer.from(bot.sessionSecretKey, "base64")
  );

  const [agentPda] = deriveAgentPda(opts.walletPda, agentPubkey);
  const [sessionPda, sessionBump] = deriveSessionPda(
    opts.walletPda,
    agentPubkey,
    sessionPubkey
  );

  const [agentAccount, sessionAccount] = await Promise.all([
    withRpcRetry(() => connection.getAccountInfo(agentPda)),
    withRpcRetry(() => connection.getAccountInfo(sessionPda)),
  ]);

  if (!agentAccount) {
    throw createApiError(
      "Live setup is incomplete. Owner approval did not register the wallet signer.",
      409
    );
  }

  if (sessionAccount) {
    return {
      sessionAddress: sessionPda.toBase58(),
      sessionPubkey: sessionPubkey.toBase58(),
      signature: "already-created",
    };
  }

  const createSessionIx = buildCreateSessionIx(
    agentPubkey,
    opts.walletPda,
    agentPda,
    sessionPda,
    sessionBump,
    sessionPubkey,
    BigInt(opts.durationSecs),
    BigInt(Math.floor(opts.maxAmountSol * LAMPORTS_PER_SOL)),
    BigInt(Math.floor(opts.maxPerTxSol * LAMPORTS_PER_SOL))
  );

  const sessionRentLamports = await withRpcRetry(() =>
    connection.getMinimumBalanceForRentExemption(154) // SessionKey::SIZE
  );
  const signerRentLamports = await withRpcRetry(() =>
    connection.getMinimumBalanceForRentExemption(0)
  );
  const requiredAgentLamports = sessionRentLamports + signerRentLamports + 500_000;
  const agentBalanceLamports = await withRpcRetry(() =>
    connection.getBalance(agentPubkey)
  );

  const tx = new Transaction();
  tx.feePayer = sessionKeypair.publicKey;

  if (agentBalanceLamports < requiredAgentLamports) {
    tx.add(SystemProgram.transfer({
      fromPubkey: sessionKeypair.publicKey,
      toPubkey: agentPubkey,
      lamports: requiredAgentLamports - agentBalanceLamports,
    }));
  }

  tx.add(createSessionIx);

  const txSender = new TransactionSender(connection);
  const txWithFees = txSender.addPriorityFee(tx);
  const result = await txSender.sendTransaction(txWithFees, [sessionKeypair, agentKeypair]);

  if (!result.success || !result.signature) {
    throw createApiError(
      `Failed to finalize live setup: ${result.error ?? "unknown error"}`,
      500
    );
  }

  await db.update(bots)
    .set({
      agentConfigAddress: agentPda.toBase58(),
      sessionAddress: sessionPda.toBase58(),
      updatedAt: new Date(),
    })
    .where(eq(bots.botId, opts.botId));

  return {
    sessionAddress: sessionPda.toBase58(),
    sessionPubkey: sessionPubkey.toBase58(),
    signature: result.signature,
  };
}

/**
 * Build a RevokeSession instruction (discriminant = 4).
 *
 * On-chain layout (1 byte):
 *   [0]  disc (4)
 *
 * Accounts (matching seal program revoke_session.rs):
 *   0. [signer]    Authority (wallet owner OR agent)
 *   1. [writable]  SessionKey PDA
 *   2. []          SmartWallet PDA
 */
function buildRevokeSessionIx(
  authority: PublicKey,
  sessionPda: PublicKey,
  walletPda: PublicKey
) {
  const data = Buffer.from([4]); // InstructionDiscriminant.RevokeSession

  return {
    programId: SEAL_PROGRAM_ID,
    keys: [
      { pubkey: authority, isSigner: true, isWritable: false },
      { pubkey: sessionPda, isSigner: false, isWritable: true },
      { pubkey: walletPda, isSigner: false, isWritable: false },
    ],
    data,
  };
}

function buildDeregisterAgentIx(
  owner: PublicKey,
  walletPda: PublicKey,
  agentPda: PublicKey
) {
  return {
    programId: SEAL_PROGRAM_ID,
    keys: [
      { pubkey: owner, isSigner: true, isWritable: true },
      { pubkey: walletPda, isSigner: false, isWritable: true },
      { pubkey: agentPda, isSigner: false, isWritable: true },
    ],
    data: Buffer.from([8]), // DeregisterAgent
  };
}

function buildCloseWalletIx(owner: PublicKey, walletPda: PublicKey) {
  return {
    programId: SEAL_PROGRAM_ID,
    keys: [
      { pubkey: owner, isSigner: true, isWritable: true },
      { pubkey: walletPda, isSigner: false, isWritable: true },
    ],
    data: Buffer.from([9]), // CloseWallet
  };
}

/**
 * POST /wallet/prepare-register-agent
 * Prepare TX to register a bot as a Seal agent on the user's wallet.
 *
 * Flow:
 * 1. Validate the bot exists, belongs to the user, and is live mode
 * 2. Derive the AgentConfig PDA
 * 3. Build the RegisterAgent instruction
 * 4. Return serialized TX for MWA signing
 * 5. Store agentPubkey + agentConfigAddress in the bot row
 */
wallet.post(
  "/prepare-register-agent",
  requireAuth,
  zValidator("json", registerAgentSchema),
  async (c) => {
    const userId = c.var.userId;
    const ownerAddress = c.var.walletAddress;
    const body = c.req.valid("json");

    const ownerPubkey = new PublicKey(ownerAddress);
    const agentPubkey = new PublicKey(body.agentPubkey);
    const allowedPrograms = resolveAgentAllowedPrograms(body.allowedPrograms);

    // ── Validate bot ──
    const [bot] = await db
      .select()
      .from(bots)
      .where(and(eq(bots.botId, body.botId), eq(bots.userId, userId)));

    if (!bot) {
      throw createApiError("Bot not found", 404);
    }

    if (bot.mode !== "live") {
      throw createApiError(
        "Agent registration is only required for live-mode bots",
        400
      );
    }

    if (bot.agentPubkey) {
      throw createApiError("Bot already has an agent registered", 409, {
        agentPubkey: bot.agentPubkey,
        agentConfigAddress: bot.agentConfigAddress,
      });
    }

    // ── Verify Seal wallet exists ──
    const walletPda = await getCanonicalWalletPda(userId, ownerPubkey);
    const connection = getConnection();
    const walletAccount = await withRpcRetry(() =>
      connection.getAccountInfo(walletPda)
    );
    if (!walletAccount) {
      throw createApiError(
        "Seal wallet not found. Create one first.",
        404
      );
    }

    // ── Derive agent PDA ──
    const [agentPda, bump] = deriveAgentPda(walletPda, agentPubkey);

    // Check if agent PDA already exists on-chain
    const existingAgent = await withRpcRetry(() =>
      connection.getAccountInfo(agentPda)
    );
    if (existingAgent) {
      throw createApiError("Agent account already exists on-chain", 409, {
        agentConfigAddress: agentPda.toBase58(),
      });
    }

    // ── Build TX ──
    const dailyLimitLamports = BigInt(
      Math.floor(body.dailyLimitSol * LAMPORTS_PER_SOL)
    );
    const perTxLimitLamports = BigInt(
      Math.floor(body.perTxLimitSol * LAMPORTS_PER_SOL)
    );

    const sponsored = false;
    const funderPubkey = ownerPubkey;

    const ix = buildRegisterAgentIx(
      ownerPubkey,
      walletPda,
      agentPubkey,
      agentPda,
      bump,
      body.name,
      dailyLimitLamports,
      perTxLimitLamports,
      allowedPrograms
    );

    const { blockhash, lastValidBlockHeight } = await withRpcRetry(() =>
      connection.getLatestBlockhash()
    );

    const tx = new Transaction();
    tx.add(ix);
    tx.recentBlockhash = blockhash;
    tx.feePayer = funderPubkey;

    const serialized = tx
      .serialize({ requireAllSignatures: false })
      .toString("base64");

    // ── Persist agent info in bot row ──
    await db.update(bots)
      .set({
        agentPubkey: agentPubkey.toBase58(),
        agentConfigAddress: agentPda.toBase58(),
        updatedAt: new Date(),
      })
      .where(eq(bots.botId, body.botId));

    return c.json({
      success: true,
      botId: body.botId,
      agentPubkey: agentPubkey.toBase58(),
      agentConfigAddress: agentPda.toBase58(),
      allowedPrograms: allowedPrograms.map((programId) => programId.toBase58()),
      transaction: serialized,
      sponsored,
      network: config.SOLANA_NETWORK,
      blockhash,
      lastValidBlockHeight,
    });
  }
);

