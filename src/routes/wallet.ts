/**
 * POST /wallet/prepare-create
 * Prepare TX for Seal wallet creation.
 * When Turnkey sponsor is configured, the TX is partially signed by
 * the sponsor (user pays nothing). Otherwise, the user pays.
 */
wallet.post(
  "/prepare-create",
  requireAuth,
  zValidator("json", createWalletSchema),
  async (c) => {
    const ownerAddress = c.var.walletAddress;
    const body = c.req.valid("json");
    const ownerPubkey = new PublicKey(ownerAddress);

    const [walletPda, bump] = deriveWalletPda(ownerPubkey);

    // Check if wallet already exists (with retry for flaky RPC)
    const connection = getConnection();
    const existing = await withRpcRetry(() =>
      connection.getAccountInfo(walletPda)
    );
    if (existing) {
      throw createApiError("Seal wallet already exists", 409, {
        walletAddress: walletPda.toBase58(),
      });
    }

    const dailyLimitLamports = BigInt(
      Math.floor(body.dailyLimitSol * LAMPORTS_PER_SOL)
    );
    const perTxLimitLamports = BigInt(
      Math.floor(body.perTxLimitSol * LAMPORTS_PER_SOL)
    );

    const sponsored = await isSponsorAvailable();
    const funderPubkey = sponsored
      ? (await getSponsorInfo()).publicKey
      : ownerPubkey;

    const ix = buildCreateWalletIx(
      funderPubkey,
      ownerPubkey,
      walletPda,
      bump,
      dailyLimitLamports,
      perTxLimitLamports
    );

    const { blockhash, lastValidBlockHeight } =
      await withRpcRetry(() => connection.getLatestBlockhash());

    const tx = new Transaction();
    tx.add(ix);
    tx.recentBlockhash = blockhash;
    tx.feePayer = funderPubkey;

    // If sponsor is available, partially sign so the user only
    // needs to add their MWA signature.
    if (sponsored) {
      await sponsorSign(tx);
    }

    const serialized = tx
      .serialize({ requireAllSignatures: false })
      .toString("base64");

    // Store seal wallet address in user record
    await db.update(users)
      .set({ sealWalletAddress: walletPda.toBase58() })
      .where(eq(users.walletAddress, ownerAddress));

    return c.json({
      success: true,
      walletAddress: walletPda.toBase58(),
      transaction: serialized,
      sponsored,
      network: config.SOLANA_NETWORK,
      blockhash,
      lastValidBlockHeight,
    });
  }
);

/**
 * GET /wallet/state
 * Get authenticated user's Seal wallet state.
 */
wallet.get("/state", requireAuth, async (c) => {
  const userId = c.var.userId;
  const ownerAddress = c.var.walletAddress;
  const ownerPubkey = new PublicKey(ownerAddress);
  const walletPda = await getCanonicalWalletPda(userId, ownerPubkey);

  const connection = getConnection();
  const accountInfo = await withRpcRetry(() =>
    connection.getAccountInfo(walletPda)
  );

  if (!accountInfo) {
    return c.json({
      success: true,
      exists: false,
      walletAddress: walletPda.toBase58(),
    });
  }

  const walletState = parseSmartWallet(
    walletPda.toBase58(),
    accountInfo.data as Buffer
  );

  return c.json({ success: true, exists: true, wallet: walletState });
});

