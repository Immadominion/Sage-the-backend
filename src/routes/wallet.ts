const SMART_WALLET_DISCRIMINATOR = Buffer.from("SealWalt");

function readU64LE(buf: Buffer, offset: number): bigint {
  return buf.readBigUInt64LE(offset);
}

function readI64LE(buf: Buffer, offset: number): bigint {
  return buf.readBigInt64LE(offset);
}

function readPubkey(buf: Buffer, offset: number): string {
  return new PublicKey(buf.subarray(offset, offset + 32)).toBase58();
}

interface SmartWalletState {
  address: string;
  pdaAuthority: string;
  owner: string;
  bump: number;
  nonce: string;
  agentCount: number;
  guardianCount: number;
  recoveryThreshold: number;
  guardians: string[];
  dailyLimitSol: number;
  perTxLimitSol: number;
  spentTodaySol: number;
  dayStartTimestamp: number;
  isLocked: boolean;
  isClosed: boolean;
}

const MEMO_PROGRAM_ID = new PublicKey(
  "MemoSq4gqABAXKb96qnH8TysNcWxMyWCqXgDLGmfcHr"
);

const CORE_AGENT_ALLOWED_PROGRAMS = [
  SystemProgram.programId,
  TOKEN_PROGRAM_ID,
  TOKEN_2022_PROGRAM_ID,
  ASSOCIATED_TOKEN_PROGRAM_ID,
  MEMO_PROGRAM_ID,
];

function parseConfiguredProgramIds(value?: string): PublicKey[] {
  if (!value) return [];

  return value
    .split(",")
    .map((programId) => programId.trim())
    .filter(Boolean)
    .map((programId) => new PublicKey(programId));
}

function uniquePubkeys(programs: PublicKey[]): PublicKey[] {
  const seen = new Set<string>();
  return programs.filter((program) => {
    const key = program.toBase58();
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

function getConfiguredAgentAllowlist(): PublicKey[] {
  const clusterSpecific =
    config.SOLANA_NETWORK === "mainnet-beta"
      ? config.SEAL_ALLOWED_PROGRAMS_MAINNET
      : config.SEAL_ALLOWED_PROGRAMS_DEVNET;

  return uniquePubkeys([
    ...CORE_AGENT_ALLOWED_PROGRAMS,
    ...parseConfiguredProgramIds(config.SEAL_ALLOWED_PROGRAMS),
    ...parseConfiguredProgramIds(clusterSpecific),
  ]);
}

function resolveAgentAllowedPrograms(extraPrograms?: string[]): PublicKey[] {
  const requestedPrograms = (extraPrograms ?? []).map(
    (programId) => new PublicKey(programId)
  );

  const allowedPrograms = uniquePubkeys([
    ...getConfiguredAgentAllowlist(),
    ...requestedPrograms,
  ]);

  const hasProtocolSpecificProgram = allowedPrograms.some(
    (program) =>
      !CORE_AGENT_ALLOWED_PROGRAMS.some((core) => core.equals(program))
  );

  if (!hasProtocolSpecificProgram) {
    throw createApiError(
      "Seal agent allowlist must include at least one protocol program. Configure SEAL_ALLOWED_PROGRAMS_MAINNET/DEVNET or pass allowedPrograms explicitly.",
      400
    );
  }

  return allowedPrograms;
}

async function getCanonicalWalletPda(
  userId: number,
  ownerPubkey: PublicKey,
  options?: { allowDerivedFallback?: boolean }
): Promise<PublicKey> {
  const [user] = await db
    .select({ sealWalletAddress: users.sealWalletAddress })
    .from(users)
    .where(eq(users.id, userId));

  if (user?.sealWalletAddress) {
    return new PublicKey(user.sealWalletAddress);
  }

  if (options?.allowDerivedFallback === false) {
    throw createApiError("Seal wallet address is not recorded for this user.", 409);
  }

  const [walletPda] = deriveWalletPda(ownerPubkey);
  return walletPda;
}

function parseSmartWallet(
  address: string,
  data: Buffer
): SmartWalletState | null {
  if (data.length < 278) return null;
  const disc = data.subarray(0, 8);
  if (!disc.equals(SMART_WALLET_DISCRIMINATOR)) return null;

  const guardianCount = data[82];
  const guardians: string[] = [];
  for (let i = 0; i < guardianCount && i < 5; i++) {
    guardians.push(readPubkey(data, 84 + i * 32));
  }

  return {
    address,
    pdaAuthority: readPubkey(data, 8),
    owner: readPubkey(data, 40),
    bump: data[72],
    nonce: readU64LE(data, 73).toString(),
    agentCount: data[81],
    guardianCount,
    recoveryThreshold: data[83],
    guardians,
    dailyLimitSol: Number(readU64LE(data, 244)) / LAMPORTS_PER_SOL,
    perTxLimitSol: Number(readU64LE(data, 252)) / LAMPORTS_PER_SOL,
    spentTodaySol: Number(readU64LE(data, 260)) / LAMPORTS_PER_SOL,
    dayStartTimestamp: Number(readI64LE(data, 268)),
    isLocked: data[276] !== 0,
    isClosed: data[277] !== 0,
  };
}

