/**
 * POST /wallet/prepare-create-session
 * Prepare TX to create a session key for an agent.
 *
 * The agent (bot keypair) is the signer/payer for session creation on-chain.
 * However, since the Flutter app signs via MWA (owner wallet), we prepare
 * the TX for the OWNER to sign — the agent keypair is stored server-side
 * and we don't require it to sign here. The Flutter app must ensure the
 * agent keypair also signs before submission.
 *
 * Flow:
 * 1. Validate bot exists, belongs to user, and has a registered agent
 * 2. Verify the bot doesn't already have an active session
 * 3. Derive the SessionKey PDA from (walletPda, agentPubkey, sessionPubkey)
 * 4. Build CreateSession instruction
 * 5. Return serialized TX for client-side signing (agent signs)
 * 6. Store sessionAddress in bot row
 */
wallet.post(
  "/prepare-create-session",
  requireAuth,
  zValidator("json", createSessionSchema),
  async (c) => {
    const userId = c.var.userId;
    const ownerAddress = c.var.walletAddress;
    const body = c.req.valid("json");

    const ownerPubkey = new PublicKey(ownerAddress);
    const sessionPubkey = new PublicKey(body.sessionPubkey);

    // ── Validate bot ──
    const [bot] = await db
      .select()
      .from(bots)
      .where(and(eq(bots.botId, body.botId), eq(bots.userId, userId)));

    if (!bot) {
      throw createApiError("Bot not found", 404);
    }

    if (bot.mode !== "live") {
      throw createApiError(
        "Session keys are only used for live-mode bots",
        400
      );
    }

    if (!bot.agentPubkey || !bot.agentConfigAddress) {
      throw createApiError(
        "Bot must have a registered agent before creating a session. " +
        "Use /wallet/prepare-register-agent first.",
        400
      );
    }

    if (bot.sessionAddress) {
      throw createApiError("Bot already has an active session", 409, {
        sessionAddress: bot.sessionAddress,
      });
    }

    // ── Verify Seal wallet exists on-chain ──
    const walletPda = await getCanonicalWalletPda(userId, ownerPubkey);
    const connection = getConnection();
    const walletAccount = await withRpcRetry(() =>
      connection.getAccountInfo(walletPda)
    );
    if (!walletAccount) {
      throw createApiError(
        "Seal wallet not found. Create one first.",
        404
      );
    }

    // ── Derive PDAs ──
    const agentPubkey = new PublicKey(bot.agentPubkey);
    const [agentPda] = deriveAgentPda(walletPda, agentPubkey);

    // Verify agent config exists on-chain
    const agentAccount = await withRpcRetry(() =>
      connection.getAccountInfo(agentPda)
    );
    if (!agentAccount) {
      throw createApiError(
        "Agent config not found on-chain. Register the agent first.",
        404,
        { expectedAddress: agentPda.toBase58() }
      );
    }

    const [sessionPda, bump] = deriveSessionPda(
      walletPda,
      agentPubkey,
      sessionPubkey
    );

    // Verify session PDA doesn't already exist on-chain
    const existingSession = await withRpcRetry(() =>
      connection.getAccountInfo(sessionPda)
    );
    if (existingSession) {
      throw createApiError("Session account already exists on-chain", 409, {
        sessionAddress: sessionPda.toBase58(),
      });
    }

    // ── Build TX ──
    const durationSecs = BigInt(body.durationSecs);
    const maxAmountLamports = BigInt(
      Math.floor(body.maxAmountSol * LAMPORTS_PER_SOL)
    );
    const maxPerTxLamports = BigInt(
      Math.floor(body.maxPerTxSol * LAMPORTS_PER_SOL)
    );

    // Validate spending limits: session limits should not exceed what
    // was set at agent registration level
    if (maxPerTxLamports > maxAmountLamports) {
      throw createApiError(
        "maxPerTxSol cannot exceed maxAmountSol",
        400
      );
    }

    const ix = buildCreateSessionIx(
      agentPubkey,  // agent is signer/payer on-chain
      walletPda,
      agentPda,
      sessionPda,
      bump,
      sessionPubkey,
      durationSecs,
      maxAmountLamports,
      maxPerTxLamports
    );

    const { blockhash, lastValidBlockHeight } = await withRpcRetry(() =>
      connection.getLatestBlockhash()
    );

    const tx = new Transaction();
    tx.add(ix);
    tx.recentBlockhash = blockhash;
    // Agent is the fee payer for session creation (they're the signer on-chain)
    tx.feePayer = agentPubkey;

    const serialized = tx
      .serialize({ requireAllSignatures: false })
      .toString("base64");

    // ── Persist session info in bot row ──
    await db.update(bots)
      .set({
        sessionAddress: sessionPda.toBase58(),
        updatedAt: new Date(),
      })
      .where(eq(bots.botId, body.botId));

    return c.json({
      success: true,
      botId: body.botId,
      sessionAddress: sessionPda.toBase58(),
      sessionPubkey: sessionPubkey.toBase58(),
      agentPubkey: bot.agentPubkey,
      durationSecs: body.durationSecs,
      maxAmountSol: body.maxAmountSol,
      maxPerTxSol: body.maxPerTxSol,
      transaction: serialized,
      blockhash,
      lastValidBlockHeight,
    });
  }
);

// ═══════════════════════════════════════════════════════════════
// Combined Live Setup (Agent + Session in one TX)
// ═══════════════════════════════════════════════════════════════

const setupLiveSchema = z.object({
  botId: z.string(),
  /** Agent spending limits */
  dailyLimitSol: z.number().positive().max(1000).default(10),
  perTxLimitSol: z.number().positive().max(100).default(2),
  /** Session config */
  sessionDurationSecs: z.number().int().positive().default(7 * 24 * 60 * 60), // 7 days
  sessionMaxAmountSol: z.number().positive().default(100),
  sessionMaxPerTxSol: z.number().positive().default(2),
  allowedPrograms: z.array(z.string().min(32).max(50)).max(16).optional(),
});

/**
 * POST /wallet/setup-live
 * One-shot live mode setup: generates agent + session keypairs SERVER-SIDE,
 * builds RegisterAgent + CreateSession in a single TX.
 *
 * The backend signs with the agent keypair. Flutter signs with the owner
 * via MWA. One MWA popup → agent + session both created atomically.
 *
 * Flow:
 * 1. Validate bot exists, belongs to user, is live mode, no agent yet
 * 2. Generate agent keypair + session keypair (server-side)
 * 3. Store private keys in DB (encrypted in production)
 * 4. Build RegisterAgent IX (owner signs) + CreateSession IX (agent signs)
 * 5. Combine in one TX, agent-sign server-side
 * 6. Return partially-signed TX for MWA signing
 *
 * After MWA signing + submission, the bot has an active session key
 * and the orchestrator can use it to sign executeViaSession trades.
 */
wallet.post(
  "/setup-live",
  requireAuth,
  zValidator("json", setupLiveSchema),
  async (c) => {
    const userId = c.var.userId;
    const ownerAddress = c.var.walletAddress;
    const body = c.req.valid("json");

    const ownerPubkey = new PublicKey(ownerAddress);
    const allowedPrograms = resolveAgentAllowedPrograms(body.allowedPrograms);

    // ── Validate bot ──
    const [bot] = await db
      .select()
      .from(bots)
      .where(and(eq(bots.botId, body.botId), eq(bots.userId, userId)));

    if (!bot) {
      throw createApiError("Bot not found", 404);
    }

    if (bot.mode !== "live") {
      throw createApiError(
        "Live setup is only for live-mode bots",
        400
      );
    }

    if (bot.agentPubkey && bot.sessionAddress) {
      throw createApiError("Bot already has agent + session configured", 409, {
        agentPubkey: bot.agentPubkey,
        sessionAddress: bot.sessionAddress,
      });
    }

    // ── Verify Seal wallet exists on-chain ──
    const walletPda = await getCanonicalWalletPda(userId, ownerPubkey);
    const connection = getConnection();
    const walletAccount = await withRpcRetry(() =>
      connection.getAccountInfo(walletPda)
    );
    if (!walletAccount) {
      throw createApiError(
        "Seal wallet not found. Create one first.",
        404
      );
    }

    // ── Generate keypairs server-side ──
    const agentKeypair = Keypair.generate();
    const sessionKeypair = Keypair.generate();

    // ── Derive PDAs ──
    const [agentPda, agentBump] = deriveAgentPda(walletPda, agentKeypair.publicKey);
    const [sessionPda, sessionBump] = deriveSessionPda(
      walletPda,
      agentKeypair.publicKey,
      sessionKeypair.publicKey
    );

    // Verify neither PDA exists on-chain
    const [existingAgent, existingSession] = await Promise.all([
      withRpcRetry(() => connection.getAccountInfo(agentPda)),
      withRpcRetry(() => connection.getAccountInfo(sessionPda)),
    ]);
    if (existingAgent) {
      throw createApiError("Agent account already exists on-chain", 409);
    }
    if (existingSession) {
      throw createApiError("Session account already exists on-chain", 409);
    }

    // ── Build RegisterAgent IX ──
    const dailyLimitLamports = BigInt(
      Math.floor(body.dailyLimitSol * LAMPORTS_PER_SOL)
    );
    const perTxLimitLamports = BigInt(
      Math.floor(body.perTxLimitSol * LAMPORTS_PER_SOL)
    );

    const registerIx = buildRegisterAgentIx(
      ownerPubkey,
      walletPda,
      agentKeypair.publicKey,
      agentPda,
      agentBump,
      `bot-${body.botId}`,
      dailyLimitLamports,
      perTxLimitLamports,
      allowedPrograms
    );

    // ── Build CreateSession IX ──
    const sessionDurationSecs = BigInt(body.sessionDurationSecs);
    const sessionMaxAmountLamports = BigInt(
      Math.floor(body.sessionMaxAmountSol * LAMPORTS_PER_SOL)
    );
    const sessionMaxPerTxLamports = BigInt(
      Math.floor(body.sessionMaxPerTxSol * LAMPORTS_PER_SOL)
    );

    const createSessionIx = buildCreateSessionIx(
      agentKeypair.publicKey,
      walletPda,
      agentPda,
      sessionPda,
      sessionBump,
      sessionKeypair.publicKey,
      sessionDurationSecs,
      sessionMaxAmountLamports,
      sessionMaxPerTxLamports
    );

    // ── Assemble TX ──
    const { blockhash, lastValidBlockHeight } = await withRpcRetry(() =>
      connection.getLatestBlockhash()
    );

    // Determine fee payer: prefer sponsor if available AND funded,
    // otherwise the owner pays (they sign via MWA anyway).
    let sponsored = await isSponsorAvailable();
    let funderPubkey = ownerPubkey;

    if (sponsored) {
      const sponsorInfo = await getSponsorInfo();
      const sponsorBalance = await withRpcRetry(() =>
        connection.getBalance(sponsorInfo.publicKey)
      );
      // Need enough for: TX fees (~15k) + agent funding (~102k) = ~120k total
      const MIN_SPONSOR_BALANCE = 50_000_000; // 0.05 SOL (safety margin for multiple ops)
      if (sponsorBalance >= MIN_SPONSOR_BALANCE) {
        funderPubkey = sponsorInfo.publicKey;
      } else {
        // Sponsor exists but not funded on this network — fall back to owner
        sponsored = false;
        funderPubkey = ownerPubkey;
        console.warn(
          `⚠️  Sponsor balance (${sponsorBalance} lamports) below minimum ` +
          `(${MIN_SPONSOR_BALANCE}) on ${config.SOLANA_NETWORK}. Owner will pay setup costs.`
        );
      }
    }

    // The agent keypair needs SOL to pay rent for the SessionKey PDA.
    // SessionKey account is 154 bytes → ~0.0015 SOL rent-exempt minimum.
    // We transfer a small buffer (0.003 SOL) from the fee payer to the
    // agent so it can fund the create_account CPI inside CreateSession.
    const sessionRentLamports = await withRpcRetry(() =>
      connection.getMinimumBalanceForRentExemption(154) // SessionKey::SIZE
    );
    // Add a small buffer for compute budget
    const agentFundingLamports = sessionRentLamports + 100_000; // ~0.0001 SOL extra

    const fundAgentIx = SystemProgram.transfer({
      fromPubkey: funderPubkey,
      toPubkey: agentKeypair.publicKey,
      lamports: agentFundingLamports,
    });

    const tx = new Transaction();
    tx.add(fundAgentIx);    // 1. Fund agent so it can pay session PDA rent
    tx.add(registerIx);     // 2. Owner registers agent on wallet
    tx.add(createSessionIx); // 3. Agent creates session key
    tx.recentBlockhash = blockhash;
    tx.feePayer = funderPubkey;

    // Backend signs with agent keypair (CreateSession requires agent as signer)
    tx.partialSign(agentKeypair);

    // Optionally sponsor-sign
    if (sponsored) {
      await sponsorSign(tx);
    }

    const serialized = tx
      .serialize({ requireAllSignatures: false })
      .toString("base64");

    // ── Persist keys in DB ──
    // Store private keys as base64-encoded Uint8Array (64 bytes: seed + pubkey)
    await db.update(bots)
      .set({
        agentPubkey: agentKeypair.publicKey.toBase58(),
        agentSecretKey: Buffer.from(agentKeypair.secretKey).toString("base64"),
        agentConfigAddress: agentPda.toBase58(),
        sessionAddress: sessionPda.toBase58(),
        sessionPubkey: sessionKeypair.publicKey.toBase58(),
        sessionSecretKey: Buffer.from(sessionKeypair.secretKey).toString("base64"),
        updatedAt: new Date(),
      })
      .where(eq(bots.botId, body.botId));

    return c.json({
      success: true,
      botId: body.botId,
      agentPubkey: agentKeypair.publicKey.toBase58(),
      agentConfigAddress: agentPda.toBase58(),
      sessionAddress: sessionPda.toBase58(),
      sessionPubkey: sessionKeypair.publicKey.toBase58(),
      allowedPrograms: allowedPrograms.map((programId) => programId.toBase58()),
      transaction: serialized,
      sponsored,
      network: config.SOLANA_NETWORK,
      blockhash,
      lastValidBlockHeight,
    });
  }
);

/**
 * POST /wallet/prepare-revoke-session
 * Prepare TX to revoke an active session key.
 *
 * Either the wallet owner or the agent can revoke a session.
 * This endpoint prepares the TX for the owner to sign via MWA.
 *
 * Flow:
 * 1. Validate bot exists, belongs to user, and has an active session
 * 2. Load the on-chain session account to extract agent + sessionPubkey
 * 3. Build RevokeSession instruction (owner as authority)
 * 4. Return serialized TX for MWA signing
 * 5. Clear sessionAddress from bot row
 */
wallet.post(
  "/prepare-revoke-session",
  requireAuth,
  zValidator("json", revokeSessionSchema),
  async (c) => {
    const userId = c.var.userId;
    const ownerAddress = c.var.walletAddress;
    const body = c.req.valid("json");

    const ownerPubkey = new PublicKey(ownerAddress);

    // ── Validate bot ──
    const [bot] = await db
      .select()
      .from(bots)
      .where(and(eq(bots.botId, body.botId), eq(bots.userId, userId)));

    if (!bot) {
      throw createApiError("Bot not found", 404);
    }

    if (!bot.sessionAddress) {
      throw createApiError("Bot has no active session to revoke", 400);
    }

    // ── Verify session account exists on-chain ──
    const sessionPda = new PublicKey(bot.sessionAddress);
    const connection = getConnection();
    const sessionAccount = await withRpcRetry(() =>
      connection.getAccountInfo(sessionPda)
    );

    if (!sessionAccount) {
      // Session doesn't exist on-chain — clean up DB and return success
      await db.update(bots)
        .set({
          sessionAddress: null,
          updatedAt: new Date(),
        })
        .where(eq(bots.botId, body.botId));

      return c.json({
        success: true,
        botId: body.botId,
        message: "Session already closed on-chain. Database cleaned up.",
        alreadyClosed: true,
      });
    }

    // Parse session to check if already revoked
    const sessionData = sessionAccount.data as Buffer;
    if (sessionData.length < 154) {
      throw createApiError("Invalid session account data", 500);
    }

    const isRevoked = sessionData[145] !== 0; // offset: 8(disc)+32(wallet)+32(agent)+32(session)+1(bump)+8(created)+8(expires)+8(max)+8(spent)+8(perTx) = 145
    if (isRevoked) {
      // Already revoked on-chain — clean up DB
      await db.update(bots)
        .set({
          sessionAddress: null,
          updatedAt: new Date(),
        })
        .where(eq(bots.botId, body.botId));

      return c.json({
        success: true,
        botId: body.botId,
        message: "Session was already revoked on-chain. Database cleaned up.",
        alreadyRevoked: true,
      });
    }

    // ── Build TX ──
    const walletPda = await getCanonicalWalletPda(userId, ownerPubkey);

    const ix = buildRevokeSessionIx(
      ownerPubkey, // owner as authority
      sessionPda,
      walletPda
    );

    const { blockhash, lastValidBlockHeight } = await withRpcRetry(() =>
      connection.getLatestBlockhash()
    );

    const tx = new Transaction();
    tx.add(ix);
    tx.recentBlockhash = blockhash;
    tx.feePayer = ownerPubkey;

    const serialized = tx
      .serialize({ requireAllSignatures: false })
      .toString("base64");

    // ── Clear session from bot row ──
    await db.update(bots)
      .set({
        sessionAddress: null,
        updatedAt: new Date(),
      })
      .where(eq(bots.botId, body.botId));

    return c.json({
      success: true,
      botId: body.botId,
      sessionAddress: bot.sessionAddress,
      transaction: serialized,
      network: config.SOLANA_NETWORK,
      blockhash,
      lastValidBlockHeight,
    });
  }
);

